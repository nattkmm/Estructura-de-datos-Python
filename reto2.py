#nombres y rut

nombres = []

rut = []

n1 = input("ingrese el primer nombre:\n")
n2 = input("ingrese el segundo nombre:\n")
n3 = input("ingrese el tercer nombre:\n")
n4 = input("ingrese el cuarto nombre:\n")
n5 = input("ingrese el quinto nombre:\n")

nombres.append(n1)
nombres.append(n2)
nombres.append(n3)
nombres.append(n4)
nombres.append(n5)

r1 = input("ingrese el primer rut:\n")
r2 = input("ingrese el segundo rut:\n")
r3 = input("ingrese el tercer rut:\n")
r4 = input("ingrese el cuarto rut:\n")
r5 = input("ingrese el quinto rut:\n")

rut.append(r1)
rut.append(r2)
rut.append(r3)
rut.append(r4)
rut.append(r5)

print ("la lista de nombres es: ", nombres)
print ("la lista de ruts es: ", rut)

nombres_ordenados = sorted(nombres)

print ("Los nombres ordenados alfabeticamente son:\n", nombres_ordenados)

ruts_ordenados = sorted (rut, reverse=True)

print ("Los rut ordenados de forma descedente son:\n", ruts_ordenados)
